<h1 class="page-title">Uitleg: Hoe werkt de Leporello</h1>

<main class="admin-main">
  <section class="admin-section">
    <h2>Wat is een Leporello?</h2>
    <p>
      Een leporello is een uitklapbaar panoramaboek: meerdere beelden die in een lange strook aan elkaar
      zijn gekoppeld. Op deze website tonen we een digitale versie daarvan: een brede panorama die je
      horizontaal kunt bekijken en ontdekken.
    </p>
  </section>

  <section class="admin-section" style="margin-top:16px;">
    <h2>Navigeren in het panorama</h2>
    <ul>
      <li><strong>Zoomen:</strong> gebruik de knoppen <em>-</em>, <em>Reset</em>, <em>+</em> boven het panorama of Ctrl+scroll op je muis/trackpad.</li>
      <li><strong>Verslepen (pannen):</strong> klik en sleep binnen het panorama om naar links of rechts te bewegen.</li>
      <li><strong>Minimap:</strong> onderaan zie je een mini-overzicht. Klik op de minimap om snel naar een positie te springen.</li>
    </ul>
  </section>

  <section class="admin-section" style="margin-top:16px;">
    <h2>Hotspots</h2>
    <p>
      Hotspots zijn interactieve punten op het panorama. Klik op een hotspot om een popup te openen met
      titel en beschrijving. In de Nederlandse taal zie je de NL-tekst; wanneer je de taal naar EN schakelt
      via de header, zie je de Engelse beschrijving.
    </p>
  </section>

  <section class="admin-section" style="margin-top:16px;">
    <h2>Beheer (admin)</h2>
    <ul>
      <li><strong>Afbeeldingen toevoegen:</strong> ga naar <code>Afbeelding toevoegen</code> in het admin-paneel om nieuwe panorama-delen toe te voegen (upload of via URL).</li>
      <li><strong>Volgorde aanpassen:</strong> sleep de afbeelding-kaarten in het admin-paneel en klik op <em>Opslaan</em>.</li>
      <li><strong>Afbeeldingen verwijderen:</strong> schakel <em>Verwijdermodus</em> in en klik op een afbeeldingskaart om deze uit de volgorde te verwijderen.</li>
      <li><strong>Hotspots beheren:</strong> pas naam, beschrijving en co√∂rdinaten aan in de admin-lijst of verplaats hotspots op het panorama via de admin-modus.</li>
    </ul>
  </section>

